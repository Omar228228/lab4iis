public class Main {
    public static void main(String[] args) {
        // Обчислення множення чисел 54 і 47
        int result = multiply(54, 47);
        
        // Виведення результату у консоль
        System.out.println("Результат множення 54 на 47: " + result);
    }
    
    // Метод для множення двох чисел
    public static int multiply(int a, int b) {
        return a * b;
    }
}
